public class Arithmetic {
    public static void main(String[] args) {
        int x = 12;
        int y = x * 2; // y= 12 * 2 = 24
        System.out.println(y);

        int z = (y-x) % 5; // (24-12)= 12 % 5 reminder 2
        System.out.println(z);

        final float pi = 3.1415F;
        float f = pi / 0.62F;
        System.out.println(f);



    }
}
