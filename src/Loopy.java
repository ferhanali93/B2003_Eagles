public class Loopy {
    public static void main(String[] args) {
       int x = 2;
        System.out.println("Before the Loop, the value of x is: " + x);

        while (x < 100 ) {
            System.out.println("In the Loop");
            System.out.println("value of x is " + x);
            x = x + 1;

        }


    }
}
